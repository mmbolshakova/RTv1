#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	ch;
	char	*a;

	i = 0;
	a = (char *)s;
	ch = (char)c;
	while (a[i] || a[i] == ch)
	{
		if (a[i] == ch)
			return (&a[i]);
		i++;
	}
	return (0);
}
